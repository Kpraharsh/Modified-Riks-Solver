% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
tol=str2double(get(handles.edit7,'string'));
th0=str2double(get(handles.edit2,'string'));

incr=str2double(get(handles.edit3,'string'));
max_iter=str2double(get(handles.edit4,'string'));

dll=str2double(get(handles.edit5,'string'));
psi=str2double(get(handles.edit6,'string'));

if handles.current_data==1
    [a_t,al_t] = arc_length(psi,dll,th0,incr,max_iter,tol);
%       x=1;
elseif handles.current_data==0
    [a_t,al_t,status]=newton(dll,th0,incr,max_iter,tol);
%   if status==1
%      f=uifigure;
%      uialert(f,'Analysis Completed','Success')
%   else
%       f=uifigure;
%       uialert(f,'Convergence not achieved within given iterations','Failed')
%   end
end
axes(handles.axes1)
plot(a_t,al_t,'x')
xlim([0 2.5])
hold on
exx=linspace(0,2.6,501);
exy=((1./sqrt(b(exx,th0)) - 1.0).*(sin(th0)-exx));
plot(exx,exy)
xlabel('Normalized Displacement')
ylabel('Normalized force')
hold off


